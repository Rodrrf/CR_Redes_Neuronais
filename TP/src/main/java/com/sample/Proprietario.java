package com.sample;

public class Proprietario {
		private String nome;
		private int numero;
		public Proprietario(String nome, int numero) {
			super();
			this.nome = nome;
			this.numero = numero;
		}
		public String getNome() {
			return nome;
		}
		public int getNumero() {
			return numero;
		}

		
}
