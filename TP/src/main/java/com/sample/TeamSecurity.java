package com.sample;

public class TeamSecurity {
		private String nomeTeam;
		private int numero;
		
		public TeamSecurity(String nomeTeam, int numero) {
			super();
			this.nomeTeam = nomeTeam;
			this.numero = numero;
		}

		public String getNomeTeam() {
			return nomeTeam;
		}

		public int getNumero() {
			return numero;
		}
		
		
		
}
